<!-- Flip behavior for tap & keyboard -->
<script>
  // flip on click/tap/Enter/Space
  document.querySelectorAll('.flip-card .flip-card-inner').forEach(inner => {
    inner.addEventListener('click', () => inner.classList.toggle('is-flipped'));
    inner.addEventListener('keydown', e => {
      if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); inner.classList.toggle('is-flipped'); }
    });
  });
</script>

